package Day1and2;

public class CaseSwitch {

	public static void main(String[] args) {
		int num = 2;
		
		switch (num) {
		case 1:
			System.out.println("Number is 1");
			break;
		case 2:
			System.out.println("Number is 2");
			break;
		case 3:
			System.out.println("Number is 3");
			break;
		default:
			System.out.println("num");
		}
	}

}
